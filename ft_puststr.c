#include "ft_printf.h"

int ft_putstr(char *s1)
{
    int i;
    i = 0;
    if (!s1)
    return write(1,"(null)",6);
    while (s1[i])
    {
        ft_putchar(s1[i]);
        i++;
        }
        return (i);
}