#include "ft_printf.h"

int	ft_putnbr(int n)
{
	long long	nb;
	int			count;

	nb = n;
	count = 0;
	if (nb < 0)
	{
		ft_putchar('-');
		count++;
		nb = -nb;
	}
	if (nb >= 10)
		count += ft_putnbr(nb / 10);
	count += ft_putchar((nb % 10) + '0');
	return (count);
}
